void SPI_write(uint8_t reg, uint8_t value){
	uint8_t data [2];
	data[0] = reg;
	data[1] = value;
	HAL_GPIO_WritePin(SPI_CS1_LPS25HB_GPIO_Port, SPI_CS1_LPS25HB_Pin, 0);
	HAL_SPI_Transmit(&hspi3, data,2, 10);
	HAL_GPIO_WritePin(SPI_CS1_LPS25HB_GPIO_Port, SPI_CS1_LPS25HB_Pin, 1);
}

uint8_t SPI_read(uint8_t reg){
	uint8_t data [2];
	//tryb odczytu
	data[0] = reg | (1<<7);
	HAL_GPIO_WritePin(SPI_CS1_LPS25HB_GPIO_Port, SPI_CS1_LPS25HB_Pin, 0);
	HAL_SPI_TransmitReceive(&hspi3, data, &data[1],1, 10);
	HAL_GPIO_WritePin(SPI_CS1_LPS25HB_GPIO_Port, SPI_CS1_LPS25HB_Pin, 1);
	return data[1];
}

float read_pressure(){
	 uint8_t pressure_raw_tab [3];
	 uint32_t pressure_raw;
	 pressure_raw_tab[0]=SPI_read(0x28);
	 pressure_raw_tab[1]=SPI_read(0x29);
	 pressure_raw_tab[2]=SPI_read(0x2A);
	 pressure_raw= (uint32_t) pressure_raw_tab[2] << 16 | (uint32_t) pressure_raw_tab[1] << 8 | (uint32_t) pressure_raw_tab[0];
	 return ((float)pressure_raw)/4096.0f;

}

float read_temperature(){
	 uint8_t temperature_raw_tab [2];
	 int16_t temperature_raw;
	 temperature_raw_tab[0]=SPI_read(0x2B);
	 temperature_raw_tab[1]=SPI_read(0x2C);
	 temperature_raw= (uint16_t) temperature_raw_tab[1] << 8 | (uint16_t) temperature_raw_tab[0] ;
	 return ((float)temperature_raw)/480.0+42.5f;
}
